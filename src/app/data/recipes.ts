export function resolveCanonicalRecipeId(id: string): string {
  return LEGACY_RECIPE_ID_ALIASES[id] ?? id;
}

/** Start Here deep link when this recipe matches a builder template row. */
export function ingredientsBuilderPathForRecipeId(recipeId: string): string | null {
  const id = resolveCanonicalRecipeId(recipeId);
  for (const [cat, list] of Object.entries(standardRecipes)) {
    if (!Array.isArray(list)) continue;
    if (list.some((r: { id: string }) => r.id === id)) {
      return `/ingredients?category=${encodeURIComponent(cat)}&recipe=${encodeURIComponent(id)}`;
    }
  }
  return null;
}

