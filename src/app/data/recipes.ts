/** Manual-only first; full builder set from `standardRecipes.ts` (no duplicate ids) */
const MANUAL_RECIPE_IDS = new Set(RECIPES_MANUAL.map((r) => r.id));
export const recipes: Recipe[] = [
  ...RECIPES_MANUAL,
  ...recipesDerivedFromStandardTemplates().filter((r) => !MANUAL_RECIPE_IDS.has(r.id)),
];

export const getRecipeById = (id: string): Recipe | undefined => {
  const resolved = resolveCanonicalRecipeId(id);
  return recipes.find((recipe) => recipe.id === resolved);
};

export const getRecipesByCategory = (category: Recipe["category"]): Recipe[] => {
  return recipes.filter((recipe) => recipe.category === category);
};
